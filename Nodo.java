import java.util.*;

// Clase para representar un nodo de la lista enlazada
class Nodo {
    int clave;// La clave del nodo
    int valor; // El valor asociado al nodo
    Nodo siguiente;// Referencia al siguiente nodo en la lista

         // Constructor de la clase Nodo
    public Nodo(int clave, int valor) {
        this.clave = clave;
        this.valor = valor;
    }
}
